# Файл File1.txt НЕ будет содержать информацию, поскольку код НЕ выполняется по причине ошибки 
# ZeroDivisionError, в конце НЕ происходит безопасное закрытие файла r.close()
r = open("path_to_file\\File1.txt",
        "a", encoding="utf-8") # открыть для дозаписи в конец файла, кодировка "utf-8"
