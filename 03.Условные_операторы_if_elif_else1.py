# СРАВНЕНИЕ И ВЫПОЛНЕНИЕ УСЛОВИЙ
# Пример_1
x = 0
if x == 0:
	print("if")
elif x > 0:
	print("elif")
else:
	print("else")
# поскольку в условии 'if' значение истинно, то дальше проверка не выполняется
# OUT: if

# Пример_2
x = 3
if x == 0:
	print("if")
elif x > 0:
	print("elif")
else:
	print("else")
# поскольку значение в 'if' ложно, далее в 'elif' истинно, то дальше проверка не выполняется
# OUT: elif

# Пример_3
x = -1
if x == 0:
	print("if")
elif x > 0:
	print("elif")
else:
	print("else")
# поскольку ВСЕ ('if' и 'elif') проверки не прошли
# OUT: else


# ИЗМЕНЕНИЕ ЗНАЧЕНИЯ ПЕРЕМЕННОЙ И ВЫПОЛНЕНИЕ УСЛОВИЙ
# Пример_1
x = 5
if x == 0:
	x += 1
# Условие проверки НЕ выполняется и 5 делится на значение "x", т.е. на 5
print(5/x) # OUT: 1.0

# Пример_2
x = 0
if x == 0:
	x += 1
# Условие проверки выполняется и 5 делится на значение "x", увеличенное на +1, т.е. на 1
print(5/x) # OUT: 5.0
