# УСЛОВНЫЕ ОПЕРАТОРЫ "if", "elif" и "else".

# Пример_1
if True:
	print("if")
elif True:
	print("elif")
else:
	print("else")
# поскольку в условии 'if' значение истинно, то дальше проверка не выполняется
# OUT: if

# Пример_2
if False:
	print("if")
elif True:
	print("elif")
else:
	print("else")
# поскольку значение в 'if' ложно, далее в 'elif' истинно, то дальше проверка не выполняется
# OUT: elif

# Пример_3
if False:
	print("if")
elif False:
	print("elif")
else:
	print("else")
# поскольку ВСЕ ('if' и 'elif') проверки не прошли
# OUT: else

